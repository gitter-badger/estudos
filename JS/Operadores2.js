var numero = 10;
var resultado;
var br = "<br>";

//somando 10 + 10
resultado = numero + numero;
document.write(resultado + br);
//subtraindo 10 - 10
resultado = numero - numero;
document.write(resultado + br);
//Multiplicando 10 * 10
resultado = numero * numero;
document.write(resultado + br);
//Dividindo 10 / 10
resultado = numero / numero;
document.write(resultado + br);
// Mod obtendo o resto da divisão de 10 / 10
resultado = numero % numero;
document.write(resultado + br);
